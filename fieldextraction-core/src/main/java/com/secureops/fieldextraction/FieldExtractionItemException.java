package com.secureops.fieldextraction;

public class FieldExtractionItemException extends Exception {

	public FieldExtractionItemException() {
		super();
	}

	public FieldExtractionItemException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FieldExtractionItemException(String message, Throwable cause) {
		super(message, cause);
	}

	public FieldExtractionItemException(String message) {
		super(message);
	}

	public FieldExtractionItemException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -1555696668089238669L;

}
